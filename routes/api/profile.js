// api/profile
// Create or edit profile

router.post(
  "/",
  passport.authenticate("jwt", { session: false }),
  (req, res) => {
    const { errors, isValid } = validateProfileInput(req.body);

    // Check Validation
    if (!isValid) {
      // Return any errors with 400 status
      return res.status(400).json(errors);
    }

    // Get fields
    const profileFields = {};
    profileFields.user = req.user.id;
    if (req.body.handle) profileFields.handle = req.body.handle;
    if (req.body.name) profileFields.name = req.body.name;
    if (req.body.avatar) profileFields.avatar = req.body.avatar;
    if (req.body.age) profileFields.age = req.body.age;
    if (req.body.location) profileFields.location = req.body.location;
    if (req.body.lookingFor) profileFields.lookingFor = req.body.lookingFor;
    if (req.body.bio) profileFields.bio = req.body.bio;
    if (req.body.artists) profileFields.artists = req.body.artists;
    if (req.body.art) profileFields.art = req.body.art;
    if (req.body.tracks) profileFields.tracks = req.body.tracks;
    if (req.body.albums) profileFields.albums = req.body.albums;

    Profile.findOne({ user: req.user.id }).then(profile => {
      if (profile) {
        // Update
        Profile.findOneAndUpdate(
          { user: req.user.id },
          { $set: profileFields },
          { new: true }
        ).then(profile => res.json(profile));
      } else {
        // Create

        // Check if handle exists
        Profile.findOne({ handle: profileFields.handle }).then(profile => {
          if (profile) {
            errors.handle = "That handle already exists";
            res.status(400).json(errors);
          }

          // Save Profile
          new Profile(profileFields).save().then(profile => res.json(profile));
        });
      }
    });
  }
);

router.delete(
  "/",
  passport.authenticate("jwt", { session: false }),
  (req, res) => {
    Profile.findOneAndRemove({ user: req.user.id }).then(() => {
      User.findOneAndRemove({ _id: req.user.id }).then(() =>
        res.json({ success: true })
      );
    });
  }
);

module.exports = router;
